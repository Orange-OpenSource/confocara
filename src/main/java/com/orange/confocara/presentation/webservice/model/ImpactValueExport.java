package com.orange.confocara.presentation.webservice.model;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.orange.confocara.connector.persistence.model.ImpactValue;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@JsonInclude(JsonInclude.Include.NON_NULL)
@NoArgsConstructor
@AllArgsConstructor
public class ImpactValueExport {
    private String reference;
    private String name;
    private boolean editable;

    public ImpactValueExport(ImpactValue impactValue) {
        reference = String.valueOf(impactValue.getId());
        name = impactValue.getName();
        editable = impactValue.isEditable();
    }
}